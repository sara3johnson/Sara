package org.example;

public class Sauces {

    public String mayo;
    public String mustard;
    public String ketchup;
    public String ranch;
    public String thousandIsland;
    public String  vinaigrette;

    public Sauces(String mayo, String mustard, String ketchup, String ranch, String thousandIsland, String vinaigrette) {
        this.mayo = mayo;
        this.mustard = mustard;
        this.ketchup = ketchup;
        this.ranch = ranch;
        this.thousandIsland = thousandIsland;
        this.vinaigrette = vinaigrette;
    }
}
// mayo, mustard, ketchup, ranch, thousand island, vinaigrette